bool capk_verify(const struct capk *pk)
{
	gcry_error_t err;
	gcry_md_hd_t mdh;
	int algo = GCRY_MD_SHA1;

	if (pk->hash_algo != HASH_SHA_1 || pk->pk_algo != PK_RSA)
		return false;

	err = gcry_md_open(&mdh, algo, 0);
	if (err) {
		fprintf(stderr, "LibGCrypt error %s/%s\n",
				gcry_strsource (err),
				gcry_strerror (err));
		return false;
	}

	gcry_md_write(mdh, pk->rid, sizeof(pk->rid));
	gcry_md_putc(mdh, pk->index);
	gcry_md_write(mdh, pk->modulus, pk->mlen);
	gcry_md_write(mdh, pk->exp, pk->elen);

	gcry_md_final(mdh);

	unsigned char *h = gcry_md_read(mdh, 0);
	if (!h)
		return false;

	bool r = memcmp(h, pk->hash, 20) ? false : true;

	gcry_md_close(mdh);

	return r;
}

void rid_verify(const struct capk *ridpks, int num)
{
	int i;

	for (i = 0; i < num; i++) {
		const struct capk *pk = &ridpks[i];
		printf("Verifying CA PK for %02hhx:%02hhx:%02hhx:%02hhx:%02hhx IDX %02hhx ...",
				pk->rid[0],
				pk->rid[1],
				pk->rid[2],
				pk->rid[3],
				pk->rid[4],
				pk->index);
		if (capk_verify(pk))
			printf("OK\n");
		else
			printf("Failed!\n");
	}
}

int main(void) {
	/* Version check should be the very first call because it
	 * makes sure that important subsystems are intialized. */
	if (!gcry_check_version (GCRYPT_VERSION)) {
		fputs ("libgcrypt version mismatch\n", stderr);
		exit (2);
	}

	/* We don't want to see any warnings, e.g. because we have not yet
	 * parsed program options which might be used to suppress such
	 * warnings. */
	gcry_control (GCRYCTL_SUSPEND_SECMEM_WARN);

	/* ... If required, other initialization goes here.  Note that the
	 * process might still be running with increased privileges and that
	 * the secure memory has not been intialized.  */

	/* Allocate a pool of 16k secure memory.  This make the secure memory
	 * available and also drops privileges where needed.  */
	gcry_control (GCRYCTL_INIT_SECMEM, 16384, 0);

	/* It is now okay to let Libgcrypt complain when there was/is
	 * a problem with the secure memory. */
	gcry_control (GCRYCTL_RESUME_SECMEM_WARN);

	/* ... If required, other initialization goes here.  */

	/* Tell Libgcrypt that initialization has completed. */
	gcry_control (GCRYCTL_INITIALIZATION_FINISHED, 0);

	rid_verify(vsdc, 4);
	rid_verify(aeips, 4);

	return 0;
}
